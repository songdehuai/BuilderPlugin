package com.builder.plugin.ext

import java.text.SimpleDateFormat
import java.util.*


fun Any?.log() {
    println("**************************************************************************************************************************************************************************************************")
    print("*\n*\n*")
    println("    $this")
    println("*    ${nowTime()}")
    print("*\n*\n")
    println("**************************************************************************************************************************************************************************************************")
}

fun Any?.lineE() {
    print("\n\n")
    println("\u001b[0;35m**************************************************************************************************************************************************************************************************\u001b[0m")
    print("\n\n")
}

fun Any?.line() {
    print("*\n*\n*")
    println("**************************************************************************************************************************************************************************************************")
    print("*\n*\n*")
}

fun Any?.logE() {
    println("\u001b[0;35m**************************************************************************************************************************************************************************************************\u001b[0m")
    print("\u001b[0;35m*\n*\n*\u001b[0m")
    println("\u001b[0;35m    $this\u001b[0m")
    println("\u001b[0;35m*    ${nowTime()}\u001b[0m")
    print("\u001b[0;35m*\n*\n\u001b[0m")
    println("\u001b[0;35m**************************************************************************************************************************************************************************************************\u001b[0m")
}

fun Any?.printE() {
    println("\u001b[0;35m${this.toString()}\u001b[0m")
}

fun Any?.emptyLine() {
    println("\n")
}

@Suppress("SimpleDateFormat")
fun nowTime(format: String? = "yyyy-MM-dd HH:mm:ss"): String {
    val sdf = SimpleDateFormat(format)
    return sdf.format(Date())
}


fun String.plusLine(): String {
    return this.plus(System.lineSeparator())
}